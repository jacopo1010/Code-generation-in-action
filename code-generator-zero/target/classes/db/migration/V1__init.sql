
CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    username VARCHAR(255) NOT NULL,
    password VARCHAR(255) NOT NULL,
    first_name VARCHAR(255),
    last_name VARCHAR(255),
    creation_time_stamp TIMESTAMP,
    last_update_time_stamp TIMESTAMP,
    PRIMARY KEY (id)
);

ALTER TABLE users
    ADD CONSTRAINT uq_users_username
    UNIQUE (username);

CREATE TABLE IF NOT EXISTS projects_users (
    projects_id BIGINT NOT NULL,
    users_id BIGINT NOT NULL,
    PRIMARY KEY (projects_id, users_id),
    FOREIGN KEY (projects_id) REFERENCES projects(id),
    FOREIGN KEY (users_id) REFERENCES users(id)
);


CREATE TABLE IF NOT EXISTS projects (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    description VARCHAR(255),
    user_id BIGINT NOT NULL,
    PRIMARY KEY (id)
);

ALTER TABLE projects
    ADD CONSTRAINT fk_projects_user_id
    FOREIGN KEY (user_id) REFERENCES users(id);


CREATE TABLE IF NOT EXISTS tasks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    description VARCHAR(255),
    completed BOOLEAN NOT NULL,
    creation_time_stamp TIMESTAMP NOT NULL,
    last_update_time_stamp TIMESTAMP NOT NULL,
    project_id BIGINT NOT NULL,
    PRIMARY KEY (id)
);

ALTER TABLE tasks
    ADD CONSTRAINT fk_tasks_project_id
    FOREIGN KEY (project_id) REFERENCES projects(id);

